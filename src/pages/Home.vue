<script>
import $ from "jquery";

export default {
  setup() {
    return {};
  },
  methods: {
    scroll() {
      let $nav = null,
        $cnt = null,
        moveCnt = null,
        moveIndex = 0,
        moveCntTop = 0,
        winH = 0,
        time = false; // 새로 만든 변수

      $(document).ready(function () {
        init();
        initEvent();
      });

      let init = () => {
        $cnt = $(".content");
        $nav = $(".header a");
      };

      let initEvent = () => {
        $("html ,body").scrollTop();
        winResize();
        $(window).resize(function () {
          winResize();
        });
        $nav.on("click", function () {
          moveIndex = $(this).parent("li").index();
          moving(moveIndex);
          return false;
        });
        $cnt.on("mousewheel", function (e) {
          if (time === false) {
            // time 변수가 펄스일때만 휠 이벤트 실행
            wheel(e);
          }
        });
      };

      let winResize = function () {
        winH = $(window).height();
        $cnt.children("div").height(winH);
        $("html ,body").scrollTop(moveIndex.scrollTop);
      };

      let wheel = (e) => {
        if (e.originalEvent.wheelDelta < 0) {
          if (moveIndex < 3) {
            moveIndex += 1;
            moving(moveIndex);
          }
        } else {
          if (moveIndex > 0) {
            moveIndex -= 1;
            moving(moveIndex);
          }
        }
      };

      let moving = (index) => {
        time = true; // 휠 이벤트가 실행 동시에 true로 변경
        moveCnt = $cnt.children("div").eq(index);
        moveCntTop = moveCnt.offset().top;
        $("html")
          .stop()
          .animate(
            {
              scrollTop: moveCntTop - 120,
            },
            1000,
            () => {
              time = false; // 휠 이벤트가 끝나면 false로 변경
            }
          );
        $nav.parent("li").eq(index).addClass("on").siblings().removeClass("on");
      };
    },
  },

  created() {
    this.scroll();
  },
};
</script>

